    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed
        String nombre, referencia, cantidad, precio;
        nombre = txtNombre.getText();
        referencia = txtReferencia.getText();
        cantidad = txtCantidad.getText();
        precio = txtPrecio.getText();

        ArrayList<Productos> productosModificado;
        try {
            if (aux == 0) {

                Productos p = new Productos(nombre, referencia, cantidad, precio) ;

                p.guardar(salida);

            } else {
                productosModificado = Helper.modificarProducto(ruta, nombre, referencia, cantidad, precio);
                salida = new ObjectOutputStream(new FileOutputStream(ruta));
                Helper.volcado(salida, productosModificado);
                aux = 0;
                Helper.mensaje(this, "Persona Actualizada Correctamente!", 1);
            }
        } catch (IOException ex) {
            Logger.getLogger(AgregarPro.class.getName()).log(Level.SEVERE, null, ex);
        }
        Helper.llenarTabla(tblProducto, ruta);

        txtNombre.setText("");
        txtReferencia.setText("");
        txtCantidad.setText("");
        txtPrecio.setText("");
        txtNombre.requestFocusInWindow();

    }//GEN-LAST:event_cmdGuardarActionPerformed

    private void cmdEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEliminarActionPerformed
        int i, op;
        op = JOptionPane.showConfirmDialog(this, "Â¿Seguro que desea eliminar este producto?", "Eliminar", JOptionPane.YES_NO_OPTION);

        ArrayList<Productos> productos = Helper.traerDatos(ruta);
        if (op == JOptionPane.YES_OPTION) {
            i = tblProducto.getSelectedRow();
            productos.remove(i);
            try {
                salida = new ObjectOutputStream(new FileOutputStream(ruta));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(AgregarPro.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(AgregarPro.class.getName()).log(Level.SEVERE, null, ex);
            }
            Helper.volcado(salida,productos);
            Helper.llenarTabla(tblProducto, ruta);
            txtNombre.setText("");
            txtReferencia.setText("");
            txtCantidad.setText("");
            txtNombre.requestFocusInWindow();
        }

    }//GEN-LAST:event_cmdEliminarActionPerformed

